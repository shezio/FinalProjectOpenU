original = json.loads(original_json)

def simplify(entry):
    # remove parentheses and their content
    no_paren = re.sub(r"\(.*?\)", "", entry)
    # replace dash types with space
    no_dash = no_paren.replace('–', ' ').replace('-', ' ')
    # split off city if there's a comma
    parts = [p.strip() for p in no_dash.split(',') if p.strip()]
    if len(parts) > 1:
        name = parts[0]
        city = parts[-1]
        simplified = f"{name} {city}"
    else:
        simplified = parts[0]
    # normalize whitespace
    return re.sub(r"\s+", " ", simplified).strip()

